fun main() {

    //Arrithmetic Operrators
    var num1 = 45
    var num2 = 32

    println(num1 + num2)
    println(num1 - num2)
    println(num1 / num2)
    println(num1 * num2)
    println(num1 % num2)
    //logical operators- and ,or ,not
    println(num1 < num2 && num2 != num1)//and
    println(num1 < num2 || num2 != num1)//or
    println(num1 < num2 || num2 != num1)//not

    //comparison operators

    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)

    //assignment operators
    var y = 25
    y += 2

    println(y)

    var x = 45
    x %= 2
    println(x)
    


}